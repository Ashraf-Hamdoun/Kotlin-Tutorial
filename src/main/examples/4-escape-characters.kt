fun main() {
    // To insert tab (equal 4 espeaces)
    println("Hello\tKotlin") // => Hello    Kotlin

    // To insert backespeace
    println("Hello Kotlin\b") // => Hello Kotli

    // To insert new line
    println("Hello\nKotlin") // every word will be printed in seaprted line

    // To insert carriage return
    println("Hello\rKotlin") // => Kotlin

    // To insert single quote character
    println("You're coding in Kotlin") // => You're coding in Kotlin

    // To insert double quote character
    println("I love \"Kotlin\"") // => I love "Kotlin"

    // To insert backslash
    println("go to E:\\ partition") // => go to E:\ partition

    // To insert dollar sign
    println("I wanna 1000\$") // => I wanna 1000$
}